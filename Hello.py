#casting, if you want to specify the data type of a variable
x = int(1)
y = float(1)
z = str(1)

print("this is an int:", x)
print("this is a float:", y)
print("this is a string:", z)
print("\n")

#type(), you can get the data type of a variable 
x = 1
y = 2.0
z = "hi"

print("x is:", type(x))
print("y is:", type(y))
print("z is:", type(z))
print("\n")

